#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");
const assets = path.join(root, "app/src/main/assets");
const outFile = path.join(root, "cloudflare/langbangml/migrations/003_seed_content_from_assets.sql");

const lessonFiles = [
  ["lesson-01.json", "pronunciation", "lesson-01", 10],
  ["lesson-02.json", "verbs", "lesson-02", 20],
  ["lesson-03.json", "adjectives", "lesson-03", 30],
  ["lesson-04.json", "adverbs", "lesson-04", 40],
  ["lesson-05.json", "phrases", "lesson-05", 50],
  ["lesson-06.json", "nouns", "lesson-06", 60],
];

const rows = [];
for (const [file, type, id, order] of lessonFiles) {
  const full = path.join(assets, file);
  if (!fs.existsSync(full)) continue;
  const payload = JSON.parse(fs.readFileSync(full, "utf8"));
  rows.push({
    version: "en-pl-v1",
    id,
    type,
    order,
    title: payload.title || id,
    summary: payload.summary || "",
    payload,
  });
}

const phraseLesson = JSON.parse(fs.readFileSync(path.join(assets, "lesson-05.json"), "utf8"));
const reversedPhraseLesson = {
  schema: "langbangml.reversed-phrases.v1",
  id: "pl-en-phrases",
  title: "Zwroty angielskie",
  summary: "Polish prompts with English study targets, generated from the existing phrase bank.",
  sourceLocale: "pl-PL",
  targetLocale: "en-US",
  groups: phraseLesson.groups.map((group) => ({
    id: group.id,
    title: group.title,
    subtitle: group.subtitle || "",
    sentences: group.sentences.map((sentence) => ({
      source: sentence.pl,
      target: sentence.en,
      sourceLocale: "pl-PL",
      targetLocale: "en-US",
    })),
  })),
};
rows.push({
  version: "pl-en-v1",
  id: "pl-en-phrases",
  type: "reversed_phrases",
  order: 10,
  title: reversedPhraseLesson.title,
  summary: reversedPhraseLesson.summary,
  payload: reversedPhraseLesson,
});

let sql = "-- Generated by cloudflare/langbangml/scripts/generate-content-seed.mjs\n";
sql += "BEGIN TRANSACTION;\n";
for (const row of rows) {
  sql += `INSERT OR REPLACE INTO content_lessons (content_version_id, lesson_id, lesson_type, sort_order, title, summary, payload_json)\n`;
  sql += `VALUES (${q(row.version)}, ${q(row.id)}, ${q(row.type)}, ${row.order}, ${q(row.title)}, ${q(row.summary)}, ${q(JSON.stringify(row.payload))});\n`;
}
sql += "COMMIT;\n";

fs.writeFileSync(outFile, sql);
console.log(`wrote ${outFile}`);

function q(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}
