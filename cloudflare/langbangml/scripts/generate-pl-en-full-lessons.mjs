function displayEnglish(value) {
  return String(value || "").replace(/^to\s+/i, "").trim();
}

function firstVariant(value) {
  return displayEnglish(value)
    .split("/")
    .map((part) => part.trim())
    .find(Boolean) || displayEnglish(value);
}

function cleanEnglishVerbBase(value) {
  return String(value || "")
    .replace(/^to\s+/i, "")
    .replace(/\([^)]*\)/g, "")
    .split("/")
    .map((part) => part.trim())
    .find(Boolean)
    ?.replace(/^to\s+/i, "")
    .trim()
    .toLowerCase() || firstVariant(value)
    .replace(/^to\s+/i, "")
    .trim()
    .toLowerCase();
}

function englishForms(base, past) {
  const table = past ? IRREGULAR_PAST : IRREGULAR_PRESENT;
  if (table[base]) return { ...table[base] };
  const form = past ? regularPast(base) : base;
  return {
    "1sg": form,
    "2sg": form,
    "3sg": past ? form : regularPresent3sg(base),
    "1pl": form,
    "2pl": form,
    "3pl": form,
  };
}

function regularPresent3sg(base) {
  if (/(s|x|ch|sh|o)$/.test(base)) return `${base}es`;
  if (/[^aeiou]y$/.test(base)) return `${base.slice(0, -1)}ies`;
  return `${base}s`;
}

function regularPast(base) {
  if (base.endsWith("e")) return `${base}d`;
  if (/[^aeiou]y$/.test(base)) return `${base.slice(0, -1)}ied`;
  return `${base}ed`;
}

function pluralizeEnglish(value) {
  const noun = firstVariant(value);
  if (!noun || /\s/.test(noun)) return noun;
  const irregular = {
    child: "children",
    man: "men",
    woman: "women",
    person: "people",
    water: "water",
    coffee: "coffee",
    tea: "tea",
    bread: "bread",
    food: "food",
    love: "love",
  };
  if (irregular[noun.toLowerCase()]) return irregular[noun.toLowerCase()];
  if (/(s|x|z|ch|sh)$/.test(noun)) return `${noun}es`;
  if (/[^aeiou]y$/.test(noun)) return `${noun.slice(0, -1)}ies`;
  return `${noun}s`;
}

function invariant(form) {
  return { "1sg": form, "2sg": form, "3sg": form, "1pl": form, "2pl": form, "3pl": form };
}

