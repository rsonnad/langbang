#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");
const assets = path.join(root, "app/src/main/assets");
const outFile = path.join(root, "cloudflare/langbangml/migrations/005_seed_pl_en_full_lessons.sql");

const FIELD_LOCALES = {
  pl: "en-US",
  en: "pl-PL",
  source: "pl-PL",
  target: "en-US",
};

function main() {
  const rows = [
    row("lesson-01", "pronunciation", 10, englishPronunciationLesson()),
    row("lesson-02", "verbs", 20, reverseVerbLesson(readLesson("lesson-02.json"))),
    row("lesson-03", "adjectives", 30, reverseAdjectiveLesson(readLesson("lesson-03.json"))),
    row("lesson-04", "adverbs", 40, reverseAdverbLesson(readLesson("lesson-04.json"))),
    row("lesson-05", "phrases", 50, reversePhrasesLesson(readLesson("lesson-05.json"))),
    row("lesson-06", "nouns", 60, reverseNounLesson(readLesson("lesson-06.json"))),
  ];

  let sql = "-- Generated by cloudflare/langbangml/scripts/generate-pl-en-full-lessons.mjs\n";
  sql += "DELETE FROM content_lessons WHERE content_version_id = 'pl-en-v1' AND lesson_id = 'pl-en-phrases';\n";
  for (const item of rows) {
    sql += "INSERT OR REPLACE INTO content_lessons (content_version_id, lesson_id, lesson_type, sort_order, title, summary, payload_json)\n";
    sql += `VALUES ('pl-en-v1', ${q(item.id)}, ${q(item.type)}, ${item.order}, ${q(item.title)}, ${q(item.summary)}, ${q(JSON.stringify(item.payload))});\n`;
  }
  sql += "UPDATE content_versions SET summary = 'Full English study content for Polish speakers, generated from the shared LangBang word and phrase bank.' WHERE id = 'pl-en-v1';\n";

  fs.writeFileSync(outFile, sql);
  console.log(`wrote ${outFile}`);
}

function row(id, type, order, payload) {
  return {
    id,
    type,
    order,
    title: payload.title || id,
    summary: payload.summary || "",
    payload,
  };
}

function readLesson(file) {
  return JSON.parse(fs.readFileSync(path.join(assets, file), "utf8"));
}

function basePayload(id, title, summary, extra) {
  return {
    id,
    title,
    summary,
    schema: "langbangml.pl-en.compat.v1",
    sourceLocale: "pl-PL",
    targetLocale: "en-US",
    targetField: "pl",
    sourceField: "en",
    fieldLocales: FIELD_LOCALES,
    ...extra,
  };
}

